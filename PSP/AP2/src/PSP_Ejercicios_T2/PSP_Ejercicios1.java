package PSP_Ejercicios_T2;

public class PSP_Ejercicios1 {
    public static void main(String[] args) {

        int number1 = 5;
        int number2 = 10;
        int sum = 0;

        for(int i = number1; i <= number2; i++) {
            sum = sum + i;
        }
        System.out.println(sum);
    }
}